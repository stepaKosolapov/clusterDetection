import numpy as np


def countEMDistance(p: int, S: np.matrix, X: np.matrix, Y: np.matrix) -> float:
    diff = np.squeeze(np.asarray(X - Y))
    diffT = diff.T
    invS = np.linalg.inv(S + np.eye(p))
    temp = np.squeeze(np.asarray(invS.dot(diffT)))
    distance = np.sqrt(np.dot(temp, diff))
    return distance


def countCore(p: int, cluster: list[np.matrix]) -> np.matrix:
    core = []
    N = len(cluster)
    for i in range(p):
        feature = 0
        for vector in cluster:
            feature += vector[0, i]
        feature = feature / N
        core.append(feature)
    return np.matrix(core)


def countCovariance(p: int, cluster: list[np.matrix], core: np.matrix) -> np.matrix:
    covMatrix = []
    N = len(cluster)
    for a in range(p):
        covMatrix.append([])
        for b in range(p):
            covAB = 0
            for vector in cluster:
                covAB += (vector[0, a] - core[0, a])*(vector[0, b] - core[0, b])
            covAB = covAB / (N - 1)
            covMatrix[a].append(covAB)
    return np.matrix(covMatrix)


def countDistances(x):
    global clusters, p, cores, covariances
    distances = []
    for i in range(len(clusters)):
        core = cores[i]
        S = covariances[i]
        distances.append(countEMDistance(p, S, x, core))
    return distances


